import { Prisma } from '@prisma/client';

/**
 * Момент времени для СЫРОГО SQL по колонкам времени — независимый от часового пояса
 * сессии БД.
 *
 * Все колонки времени в схеме — `timestamp` БЕЗ пояса, и Prisma хранит в них UTC-стенное
 * время. Но параметр-`Date` (как и `now()`/`CURRENT_TIMESTAMP`) прилетает в Postgres
 * типом `timestamptz`, поэтому на каждом стыке «наивная колонка ↔ значение с поясом»
 * Postgres молча доворачивает значение ПОЯСОМ СЕССИИ. При TimeZone=UTC это тождество —
 * поэтому и работало, — но на сервере с местным поясом (у нас +05) значения разъезжаются
 * на смещение: окна продаж открываются на 5 часов не тогда, время уведомлений уезжает
 * в будущее, отложенная работа стреляет не в свой срок.
 *
 * Явное `AT TIME ZONE 'UTC'` даёт наивное UTC-значение — ровно тот же байт, что пишет
 * ORM-путь Prisma, при ЛЮБОМ поясе сессии.
 *
 * ПРАВИЛО: в сыром SQL по колонкам времени НИКОГДА не использовать ни `now()`, ни голый
 * параметр-`Date` — только `utcTs(d)`. На старте `DatabaseModule` дополнительно
 * предупреждает, если пояс сессии не UTC.
 */
export const utcTs = (d: Date) => Prisma.sql`${d}::timestamptz AT TIME ZONE 'UTC'`;
