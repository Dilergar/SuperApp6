// Lottie effect assets (generated by apps/web/scripts/gen-lottie-skins.cjs, served from /public).
const EFFECT = { 'Цветочный': '/skins/petals.json', 'Ретро-неон': '/skins/neon.json' };

async function main() {
  for (const s of SKINS) {
    const existing = await prisma.cardSkin.findUnique({ where: { id: s.id }, select: { minted: true } });
    // Never lower supply below copies already issued — that would retroactively
    // mark the skin "sold out" for existing owners. Clamp up to current minted.
    const safeSupply =
      s.supply != null && existing && existing.minted > s.supply ? existing.minted : s.supply;
    const data = {
      name: s.name,
      description: s.description,
      rarity: s.rarity,
      priceAmount: s.priceAmount,
      supply: safeSupply,
      sortOrder: s.sortOrder,
      decor: s.decor,
      tokens: s.tokens,
      effectUrl: EFFECT[s.name] ?? null,
      status: 'active',
    };
    await prisma.cardSkin.upsert({
      where: { id: s.id },
      // Keep `minted` as-is on update (don't reset issued copies); refresh design fields.
      update: data,
      create: { id: s.id, ...data },
    });
    console.log(`✓ ${s.name} (${s.rarity}${s.supply ? `, тираж ${s.supply}` : ''})`);
  }
  console.log('✅ Сиды скинов применены');
}
main()
  .catch((e) => { console.error('FATAL', e); process.exit(1); })
  .finally(() => prisma.$disconnect());
