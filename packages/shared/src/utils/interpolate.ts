// ============================================================
// Единый рендер шаблонов «{{placeholder}}» — один движок на весь проект.
// Потребители: NOTIFICATION_REGISTRY (уведомления), CHATTER_REGISTRY (хроника),
// шаги «Процессов». Раньше каждый держал свою байт-в-байт копию regex-замены —
// расхождение обработки пропущенного ключа рассинхронизировало тексты одного
// события в уведомлении, плашке чата и журнале.
// ============================================================

/**
 * Подставляет `{{key}}` из `vars`. Правила (единые для всех потребителей):
 *  - отсутствующий / null / undefined ключ → пустая строка (клиент не видит `{{...}}`);
 *  - объект / массив → пустая строка (НИКОГДА не «[object Object]» в UI);
 *  - строка / число / boolean → String(value).
 * Ключи с точками ищутся плоско (vars['a.b']) — вложенный доступ не поддерживается.
 */
export function interpolateTemplate(
  template: string,
  vars: Record<string, unknown>,
): string {
  return template.replace(/\{\{\s*([\w.]+)\s*\}\}/g, (_, key: string) => {
    const value = vars[key];
    if (value === undefined || value === null) return '';
    if (typeof value === 'object') return '';
    return String(value);
  });
}
